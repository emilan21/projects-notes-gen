def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", required=True)
    ap.add_argument("--overwrite", action="store_true")
    args = ap.parse_args()

    out_dir = Path(args.out).expanduser().resolve()
    ensure_dir(out_dir)

    seen: Set[str] = set()
    unique_projects: List[Project] = []
    for p in PROJECTS:
        if p.name not in seen:
            seen.add(p.name)
            unique_projects.append(p)

    for p in unique_projects:
        path = out_dir / f"{slugify_filename(p.name)}.md"
        write_file(path, render_project_note(p), args.overwrite)

    write_file(out_dir / "Programming Project Index.md",
               render_project_index(unique_projects),
               args.overwrite)

    write_file(out_dir / "Programming Concepts Index.md",
               render_concepts_index(unique_projects),
               args.overwrite)

    write_file(out_dir / "Project Template.md",
               render_template(),
               args.overwrite)

    print(f"Wrote {len(unique_projects)} projects to {out_dir}")
