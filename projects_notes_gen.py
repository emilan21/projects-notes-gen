def render_project_note(p: Project) -> str:
    # YAML frontmatter — SAFE, simple lists only
    fm_lines = [
        "---",
        "type: project",
        f"domain: {p.domain}",
        "interfaces:",
        "  - cli",
        "  - api",
        "  - gui",
        "concepts:",
        *[f"  - {c}" for c in p.concepts],
        "sources:",
        *[f"  - {s.name}" for s in p.sources],
        "---",
        "",
    ]
    fm = "\n".join(fm_lines)

    sources_md = [
        f"- [{s.name}]({s.url})" if s.url else f"- {s.name}"
        for s in p.sources
    ]

    body = f"""# {p.name}

## Summary
{p.summary}

## Interfaces
- CLI
- API
- Desktop GUI

## Key Features
{md_list(p.key_features)}

## Core Concepts
{md_list(obs_link(c) for c in p.concepts)}

## Architecture Notes
- Core library contains domain logic and storage
- CLI / API / GUI are thin adapters
- Prefer event streams for live output

## Milestone MVP
- [ ] Core logic
- [ ] CLI interface
- [ ] API interface
- [ ] Desktop GUI interface
- [ ] Error handling
- [ ] Tests

## Extra Credit Ideas
{md_list(p.extra_credit) if p.extra_credit else "- (add your own)"}

## Related Projects
{md_list(obs_link(r) for r in p.related) if p.related else "- (none)"}

## Sources
{chr(10).join(sources_md)}

## Notes
- Implementation notes go here
"""

    return fm + body


def render_project_index(projects: List[Project]) -> str:
    grouped: Dict[str, List[str]] = {}
    for p in projects:
        grouped.setdefault(p.domain, []).append(p.name)

    lines = [
        "# Programming Project Index",
        "",
        "Central hub for all project notes.",
        "",
    ]

    for key, title in DOMAIN_SECTIONS:
        if key not in grouped:
            continue
        lines.append(f"## {title}")
        for name in sorted(grouped[key]):
            lines.append(f"- {obs_link(name)}")
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def render_concepts_index(projects: List[Project]) -> str:
    concept_map: Dict[str, Set[str]] = {}
    for p in projects:
        for c in p.concepts:
            concept_map.setdefault(c, set()).add(p.name)

    lines = ["# Programming Concepts Index", ""]
    for concept in sorted(concept_map):
        lines.append(f"## {concept}")
        for proj in sorted(concept_map[concept]):
            lines.append(f"- {obs_link(proj)}")
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


